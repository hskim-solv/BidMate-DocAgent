#!/usr/bin/env python3
"""Render the real-data eval history table into the public doc.

Reads all aggregate snapshots under `reports/real100/history/` and
writes a chronological markdown table into
`docs/private-100-doc-experiments.md` between the
``<!-- real-eval-history-start -->`` /
``<!-- real-eval-history-end -->`` HTML comment markers.

Aggregate-only by construction — only the same allowlisted fields
that ``scripts/run_real_eval_delta.py`` extracts are read. Per-case
data, query text, and doc IDs never enter the rendered table.

Modes:

* (default) overwrite the table between the markers.
* ``--check``: fail with non-zero exit if the rendered output would
  differ from what is currently committed. Suitable for CI / make
  target gating.

Usage:

    python3 scripts/render_real_eval_history.py
    python3 scripts/render_real_eval_history.py --check
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any

# Reuse the extractor + privacy guards from the delta script. Adding a
# new entry point should not weaken the boundary.
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
from scripts._utils import render_history_table  # noqa: E402
from scripts.run_real_eval_delta import extract_aggregate  # noqa: E402

HISTORY_DIR = Path("reports/real100/history")
DOC_PATH = Path("docs/private-100-doc-experiments.md")
MARKER_START = "<!-- real-eval-history-start -->"
MARKER_END = "<!-- real-eval-history-end -->"

# Columns surfaced in the chronological table. Keep narrow — wide
# tables become unreadable on GitHub.
COLUMNS: list[tuple[str, str]] = [
    ("date", "Date"),
    ("commit", "Commit"),
    ("num_predictions", "N"),
    ("accuracy", "Accuracy"),
    ("groundedness", "Grounded"),
    ("abstention", "Abst."),
    ("answer_format_compliance", "Format"),
    ("retry", "Retry"),
    # ADR 0006: LLM-judge agreement. Shown only when present; absent
    # entries render as "—" via render_history_table's fmt_cell fallback.
    ("agreement_with_verifier", "Judge⇄Verifier"),
]


def load_history() -> list[dict[str, Any]]:
    """Load every aggregate snapshot under :data:`HISTORY_DIR`.

    Re-runs ``extract_aggregate`` on each file as a defense-in-depth
    privacy check. Returns rows sorted by ``provenance.generated_at``.
    """
    if not HISTORY_DIR.exists():
        return []
    rows: list[dict[str, Any]] = []
    for path in sorted(HISTORY_DIR.glob("*.aggregate.json")):
        raw = json.loads(path.read_text(encoding="utf-8"))
        agg = extract_aggregate(raw)
        provenance = raw.get("provenance") or {}
        judge_block = agg.get("judge") or {}
        rows.append(
            {
                "file": path.name,
                "commit": str(provenance.get("git_commit") or "")[:12],
                "date": str(provenance.get("generated_at") or "")[:10],
                "num_predictions": agg.get("num_predictions"),
                "accuracy": agg.get("accuracy"),
                "groundedness": agg.get("groundedness"),
                "abstention": agg.get("abstention"),
                "answer_format_compliance": agg.get("answer_format_compliance"),
                "retry": agg.get("retry"),
                "agreement_with_verifier": judge_block.get("agreement_with_verifier"),
            }
        )
    rows.sort(key=lambda row: (row["date"], row["file"]))
    return rows


def render_table(rows: list[dict[str, Any]]) -> str:
    return render_history_table(
        rows,
        COLUMNS,
        empty_message=(
            "_No real-data history entries yet. Run "
            "`make real-eval-baseline-update` to seed the first "
            "snapshot._"
        ),
    )


def render_section(rows: list[dict[str, Any]]) -> str:
    return (
        f"{MARKER_START}\n\n"
        "Auto-generated by `scripts/render_real_eval_history.py`. "
        "Each row is one committed aggregate snapshot under "
        "`reports/real100/history/`. Aggregate-only per ADR 0005 — "
        "per-case data is never read by this script.\n\n"
        f"{render_table(rows)}\n\n"
        f"{MARKER_END}"
    )


def splice(doc_text: str, section: str) -> str:
    """Replace the marker block in *doc_text* with *section*.

    If the markers are missing, append a new "Real-data Eval History"
    section at the end of the doc so the first invocation seeds the
    surface.
    """
    pattern = re.compile(
        rf"{re.escape(MARKER_START)}.*?{re.escape(MARKER_END)}",
        re.DOTALL,
    )
    if pattern.search(doc_text):
        return pattern.sub(section, doc_text)
    appendix = (
        "\n\n## Real-data Eval History\n\n"
        "Chronological record of real-data aggregate snapshots committed under "
        "`reports/real100/history/`. The table is auto-generated; do not edit "
        "between the markers below. Each row corresponds to one deliberate "
        "`make real-eval-baseline-update` invocation, so the chain shows how "
        "real-data metrics moved as the repo changed.\n\n"
        f"{section}\n"
    )
    if not doc_text.endswith("\n"):
        doc_text += "\n"
    return doc_text + appendix


def parse_args() -> argparse.Namespace:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if the rendered table differs from what's on disk.",
    )
    ap.add_argument(
        "--doc",
        default=str(DOC_PATH),
        help="Path to the doc that holds the history section.",
    )
    return ap.parse_args()


def main() -> int:
    args = parse_args()
    doc_path = Path(args.doc)
    if not doc_path.exists():
        print(f"[ERROR] Doc not found: {doc_path}", file=sys.stderr)
        return 2
    rows = load_history()
    section = render_section(rows)
    current = doc_path.read_text(encoding="utf-8")
    next_text = splice(current, section)
    if next_text == current:
        print("[OK] Real-data eval history already up to date.")
        return 0
    if args.check:
        print(
            "[FAIL] Real-data eval history is stale.\n"
            "Run: python3 scripts/render_real_eval_history.py",
            file=sys.stderr,
        )
        return 1
    doc_path.write_text(next_text, encoding="utf-8")
    print(f"[OK] Wrote {len(rows)} entries to {doc_path}.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
