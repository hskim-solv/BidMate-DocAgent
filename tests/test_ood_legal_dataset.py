"""ADR 0046 OOD legal contract corpus regression (issue #848).

Guards the shape of ``data/ood_synthetic_legal/`` so the generator
script does not silently drift out of the contract that E3 / E4 PRs
will later consume.
"""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

import pytest


REPO_ROOT = Path(__file__).resolve().parents[1]
CORPUS_DIR = REPO_ROOT / "data" / "ood_synthetic_legal"
GENERATOR = REPO_ROOT / "scripts" / "generate_ood_legal.py"


def _doc_files() -> list[Path]:
    return sorted(CORPUS_DIR.glob("legal-*.json"))


def test_corpus_contains_exactly_50_documents() -> None:
    assert len(_doc_files()) == 50


def test_corpus_has_manifest() -> None:
    manifest_path = CORPUS_DIR / "manifest.json"
    assert manifest_path.exists(), "manifest.json is generated by the script and must be present"
    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    assert manifest["count"] == 50
    assert manifest["seed"] == 20260515


def test_corpus_has_readme() -> None:
    assert (CORPUS_DIR / "README.md").exists()


def test_corpus_has_ten_documents_per_category() -> None:
    by_cat: dict[str, int] = {}
    for path in _doc_files():
        doc = json.loads(path.read_text(encoding="utf-8"))
        cat = doc["metadata"]["category"]
        by_cat[cat] = by_cat.get(cat, 0) + 1
    assert by_cat == {
        "service_tos": 10,
        "nda": 10,
        "consortium": 10,
        "data_processing": 10,
        "sla": 10,
    }


REQUIRED_DOC_KEYS = {"doc_id", "title", "agency", "project", "metadata", "sections"}
REQUIRED_METADATA_KEYS = {
    "domain",
    "document_type",
    "category",
    "category_label_ko",
    "party_a",
    "party_b",
    "effective_date",
    "amount_krw",
    "instance_index",
}


@pytest.mark.parametrize("path", _doc_files(), ids=lambda p: p.name)
def test_every_document_matches_required_schema(path: Path) -> None:
    doc = json.loads(path.read_text(encoding="utf-8"))
    assert REQUIRED_DOC_KEYS.issubset(doc.keys()), (
        f"{path.name} missing keys: {REQUIRED_DOC_KEYS - doc.keys()}"
    )
    assert REQUIRED_METADATA_KEYS.issubset(doc["metadata"].keys()), (
        f"{path.name} missing metadata keys: {REQUIRED_METADATA_KEYS - doc['metadata'].keys()}"
    )
    assert doc["metadata"]["document_type"] == "synthetic_legal_contract"
    assert doc["metadata"]["domain"] == "korean_legal_contract"
    sections = doc["sections"]
    assert isinstance(sections, list) and len(sections) == 5
    for sec in sections:
        assert sec["heading"].startswith("제")
        assert len(sec["text"]) >= 30  # Korean text floor — guard accidental empty/short clauses


def test_doc_ids_unique() -> None:
    ids = [json.loads(p.read_text(encoding="utf-8"))["doc_id"] for p in _doc_files()]
    assert len(set(ids)) == len(ids)


def test_party_a_and_b_differ() -> None:
    """Same-party contracts are nonsensical; the generator must pick distinct sides."""
    for path in _doc_files():
        doc = json.loads(path.read_text(encoding="utf-8"))
        assert doc["metadata"]["party_a"] != doc["metadata"]["party_b"], path.name


def test_generator_is_deterministic(tmp_path: Path) -> None:
    """Re-running the generator with the same flags must produce byte-identical files."""
    out_dir = tmp_path / "ood_legal_regen"
    result = subprocess.run(
        [sys.executable, str(GENERATOR), "--output", str(out_dir)],
        capture_output=True,
        text=True,
        cwd=str(REPO_ROOT),
    )
    assert result.returncode == 0, result.stderr

    committed_files = {p.name: p.read_bytes() for p in CORPUS_DIR.glob("*.json")}
    regenerated = {p.name: p.read_bytes() for p in out_dir.glob("*.json")}
    assert regenerated.keys() == committed_files.keys()
    for name in committed_files:
        assert regenerated[name] == committed_files[name], (
            f"{name} drifted between commit and regeneration — generator is not deterministic"
        )
